// ─── Helper Functions ───────────────────────────────────────────────────────

export function getTonguePositionById(id: string): TonguePosition | undefined {
  return tonguePositions.find((tp) => tp.id === id);
}

export function getTonguePositionsByCategory(category: TonguePosition['category']): TonguePosition[] {
  return tonguePositions.filter((tp) => tp.category === category);
}

export function getSimilarSounds(id: string): TonguePosition[] {
  const sound = getTonguePositionById(id);
  if (!sound?.similarSounds) return [];
  return sound.similarSounds
    .map((sid) => getTonguePositionById(sid))
    .filter((s): s is TonguePosition => s !== undefined);
}

export function getConsonants(): TonguePosition[] {
  return tonguePositions.filter((tp) => tp.type === 'consonant' || tp.type === 'double');
}

export function getVowels(): TonguePosition[] {
  return tonguePositions.filter((tp) => tp.type === 'vowel');
}

