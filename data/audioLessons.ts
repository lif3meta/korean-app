export function getTotalAudioEpisodes(): number {
  return audioLessonSeries.reduce((sum, series) => sum + series.episodes.length, 0);
}

export function getAudioEpisodeById(id: string): { series: AudioSeries; episode: import('./audioLessons').AudioEpisode } | null {
  for (const series of audioLessonSeries) {
    const episode = series.episodes.find(ep => ep.id === id);
    if (episode) return { series, episode };
  }
  return null;
}
