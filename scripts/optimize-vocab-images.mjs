import sharp from 'sharp';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const VOCAB_DIR = path.join(ROOT, 'assets/images/vocab');
const OUTPUT_DIR = path.join(ROOT, 'assets/images/vocab-webp');

const TARGET_SIZE = 360; // 120px display * 3x retina
const WEBP_QUALITY = 80;

async function optimizeAll() {
  const categories = fs.readdirSync(VOCAB_DIR).filter(f =>
    fs.statSync(path.join(VOCAB_DIR, f)).isDirectory()
  );

  let totalOriginal = 0;
  let totalOptimized = 0;
  let fileCount = 0;

  for (const category of categories) {
    const catDir = path.join(VOCAB_DIR, category);
    const outDir = path.join(OUTPUT_DIR, category);
    fs.mkdirSync(outDir, { recursive: true });

    const files = fs.readdirSync(catDir).filter(f => f.endsWith('.png'));

    const promises = files.map(async (file) => {
      const inputPath = path.join(catDir, file);
      const outputFile = file.replace('.png', '.webp');
      const outputPath = path.join(outDir, outputFile);

      const originalSize = fs.statSync(inputPath).size;

      await sharp(inputPath)
        .resize(TARGET_SIZE, TARGET_SIZE, { fit: 'cover' })
        .webp({ quality: WEBP_QUALITY })
        .toFile(outputPath);

      const optimizedSize = fs.statSync(outputPath).size;
      return { originalSize, optimizedSize };
    });

    const results = await Promise.all(promises);
    for (const r of results) {
      totalOriginal += r.originalSize;
      totalOptimized += r.optimizedSize;
      fileCount++;
    }

    console.log(`✓ ${category}: ${files.length} images`);
  }

  console.log(`\nDone! ${fileCount} images optimized`);
  console.log(`Original:  ${(totalOriginal / 1024 / 1024).toFixed(1)} MB`);
  console.log(`Optimized: ${(totalOptimized / 1024 / 1024).toFixed(1)} MB`);
  console.log(`Savings:   ${((1 - totalOptimized / totalOriginal) * 100).toFixed(1)}%`);

  // Generate the new vocabImages.ts mapping
  generateMapping();
}

function generateMapping() {
  const categories = fs.readdirSync(OUTPUT_DIR).filter(f =>
    fs.statSync(path.join(OUTPUT_DIR, f)).isDirectory()
  );

  const lines = [
    '// Auto-generated by scripts/optimize-vocab-images.mjs',
    '// Maps vocabulary word IDs to optimized WebP images (360x360)',
    '',
    'const vocabImages: Record<string, any> = {',
  ];

  for (const category of categories) {
    const catDir = path.join(OUTPUT_DIR, category);
    const files = fs.readdirSync(catDir).filter(f => f.endsWith('.webp')).sort((a, b) => {
      const numA = parseInt(a.match(/_(\d+)\./)?.[1] || '0');
      const numB = parseInt(b.match(/_(\d+)\./)?.[1] || '0');
      return numA - numB;
    });

    for (const file of files) {
      const id = file.replace('.webp', '');
      lines.push(`  '${id}': require('@/assets/images/vocab-webp/${category}/${file}'),`);
    }
  }

  lines.push('};');
  lines.push('');
  lines.push('export function getVocabImage(id: string): any {');
  lines.push('  return vocabImages[id] || null;');
  lines.push('}');
  lines.push('');

  const outputPath = path.join(ROOT, 'data/vocabImages.ts');
  fs.writeFileSync(outputPath, lines.join('\n'));
  console.log(`\nUpdated data/vocabImages.ts`);
}

optimizeAll().catch(console.error);
