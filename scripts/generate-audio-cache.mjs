#!/usr/bin/env node

/**
 * Auto-generate lib/audioCache.ts from the audio manifest and actual files on disk.
 *
 * This reads assets/audio/ko/_manifest.json and generates the TypeScript file
 * with all require() statements that Metro needs for bundling.
 *
 * Usage: node scripts/generate-audio-cache.mjs
 * Run this after generate-audio.mjs whenever new audio files are added.
 */

import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const AUDIO_DIR = join(import.meta.dirname, '..', 'assets', 'audio', 'ko');
const OUTPUT_FILE = join(import.meta.dirname, '..', 'lib', 'audioCache.ts');

// Read manifest (maps ID → Korean text)
const manifest = JSON.parse(readFileSync(join(AUDIO_DIR, '_manifest.json'), 'utf-8'));

// Get all actual MP3 files on disk
const mp3Files = readdirSync(AUDIO_DIR)
  .filter(f => f.endsWith('.mp3'))
  .map(f => f.replace('.mp3', ''));

console.log(`📋 Manifest entries: ${Object.keys(manifest).length}`);
console.log(`📁 MP3 files on disk: ${mp3Files.length}`);

// Build reverse map: Korean text → file ID
const textToId = {};
for (const [id, text] of Object.entries(manifest)) {
  textToId[text] = id;
}

// Build Hangul ID map
const hangulIds = mp3Files.filter(f =>
  f.startsWith('c_') || f.startsWith('dc_') || f.startsWith('v_') || f.startsWith('cv_')
);

// Generate the TypeScript file
let output = `// AUTO-GENERATED by scripts/generate-audio-cache.mjs — DO NOT EDIT MANUALLY
// Run: node scripts/generate-audio-cache.mjs

/**
 * Audio cache for pre-generated Edge TTS files.
 * ${mp3Files.length} MP3 files bundled with the app.
 * Zero API cost, instant playback, offline-capable.
 */

// Map of Korean text → audio file ID
const AUDIO_MANIFEST: Record<string, string> = {\n`;

// Group by category for readability
const categories = {};
for (const [id, text] of Object.entries(manifest)) {
  const prefix = id.split('_')[0];
  if (!categories[prefix]) categories[prefix] = [];
  categories[prefix].push({ id, text });
}

const seenTexts = new Set();
for (const [prefix, items] of Object.entries(categories)) {
  output += `  // ${prefix}\n`;
  for (const { id, text } of items) {
    if (seenTexts.has(text)) continue;
    seenTexts.add(text);
    output += `  ${JSON.stringify(text)}: '${id}',\n`;
  }
}

output += `};\n\n`;

// English audio manifest (for English translations/narration)
output += `const ENGLISH_AUDIO_MANIFEST: Record<string, string> = {\n`;
const seenEnglish = new Set();
for (const [id, text] of Object.entries(manifest)) {
  if (!id.includes('_en_') && !id.startsWith('en_')) continue;
  if (seenEnglish.has(text)) continue;
  seenEnglish.add(text);
  output += `  ${JSON.stringify(text)}: '${id}',\n`;
}
output += `};\n\n`;

// Hangul ID map
output += `// Map of Hangul character ID → audio file ID\nconst HANGUL_ID_MAP: Record<string, string> = {\n`;
for (const id of hangulIds) {
  output += `  '${id}': '${id}',\n`;
}
output += `};\n\n`;

// Lookup functions
output += `// Check if we have a cached audio file for a given Korean text
export function hasCachedAudio(text: string): boolean {
  return text.trim() in AUDIO_MANIFEST;
}

export function hasCachedHangulAudio(characterId: string): boolean {
  return characterId in HANGUL_ID_MAP;
}

export function getCachedAudioId(text: string): string | null {
  const trimmed = text.trim();
  return AUDIO_MANIFEST[trimmed] || AUDIO_MANIFEST[trimmed.replace(/[.?!,]+$/, '')] || null;
}

export function getCachedEnglishAudioId(text: string): string | null {
  return ENGLISH_AUDIO_MANIFEST[text.trim()] || null;
}

export function getCachedHangulAudioId(characterId: string): string | null {
  return HANGUL_ID_MAP[characterId] || null;
}

const _uriCache: Record<string, string> = {};

export async function getCachedAudioUri(fileId: string): Promise<string | null> {
  if (_uriCache[fileId]) return _uriCache[fileId];
  try {
    const { getAudioAsset } = require('@/lib/audioAssets');
    const assetModule = getAudioAsset(fileId);
    if (!assetModule) return null;
    const Asset = require('expo-asset').Asset;
    const asset = Asset.fromModule(assetModule);
    await asset.downloadAsync();
    const uri = asset.localUri || asset.uri;
    if (uri) _uriCache[fileId] = uri;
    return uri;
  } catch (err) {
    console.warn(\`[AudioCache] Failed to load \${fileId}:\`, err);
    return null;
  }
}

// Get list of all audio IDs that exist on disk
export function getAllCachedIds(): string[] {
  return Object.values(AUDIO_MANIFEST);
}

// Get list of all Korean texts that have cached audio
export function getAllCachedTexts(): string[] {
  return Object.keys(AUDIO_MANIFEST);
}

`;

writeFileSync(OUTPUT_FILE, output);

// Also regenerate audioAssets.ts with ALL mp3 files on disk
const ASSETS_FILE = join(import.meta.dirname, '..', 'lib', 'audioAssets.ts');
let assetsOutput = `// AUTO-GENERATED by scripts/generate-audio-cache.mjs — DO NOT EDIT MANUALLY

type AudioAssetMap = Record<string, any>;

let _audioAssets: AudioAssetMap | null = null;

export function getAudioAsset(fileId: string): any {
  if (!_audioAssets) { _audioAssets = buildAudioAssets(); }
  return _audioAssets[fileId] || null;
}

export function hasAudioAsset(fileId: string): boolean {
  if (!_audioAssets) { _audioAssets = buildAudioAssets(); }
  return fileId in _audioAssets;
}

export function getAllAudioIds(): string[] {
  if (!_audioAssets) { _audioAssets = buildAudioAssets(); }
  return Object.keys(_audioAssets);
}

function buildAudioAssets(): AudioAssetMap {
  return {\n`;

for (const id of mp3Files.sort()) {
  assetsOutput += `    '${id}': require('@/assets/audio/ko/${id}.mp3'),\n`;
}

assetsOutput += `  };\n}\n`;

writeFileSync(ASSETS_FILE, assetsOutput);

console.log(`\n✅ Generated ${OUTPUT_FILE}`);
console.log(`   ${Object.keys(textToId).length} text-to-ID mappings`);
console.log(`✅ Generated ${ASSETS_FILE}`);
console.log(`   ${mp3Files.length} require() entries`);

// Report orphaned files (on disk but not in manifest)
const manifestIds = new Set(Object.keys(manifest));
const orphaned = mp3Files.filter(f => !manifestIds.has(f));
if (orphaned.length > 0) {
  console.log(`\n⚠️  ${orphaned.length} files on disk but NOT in manifest (will still be bundled):`);
  orphaned.forEach(f => console.log(`   - ${f}`));
}

// Report missing files (in manifest but not on disk)
const diskSet = new Set(mp3Files);
const missing = Object.keys(manifest).filter(id => !diskSet.has(id));
if (missing.length > 0) {
  console.log(`\n❌ ${missing.length} manifest entries with NO file on disk:`);
  missing.forEach(id => console.log(`   - ${id}: "${manifest[id]}"`));
}
