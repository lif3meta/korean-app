import { GoogleGenAI } from '@google/genai';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');

const envFile = fs.readFileSync(path.join(ROOT, '.env.local'), 'utf8');
const API_KEY = envFile.match(/GEMINI_API_KEY=(.+)/)?.[1]?.trim();
if (!API_KEY) { console.error('GEMINI_API_KEY not found in .env.local'); process.exit(1); }

const client = new GoogleGenAI({ apiKey: API_KEY });
const VOCAB_DIR = path.join(ROOT, 'assets', 'images', 'vocab');
const CONCURRENCY = parseInt(process.env.CONCURRENCY || '4', 10);

// â”€â”€ Parse all vocab files â”€â”€
function parseWordsFromFile(filePath) {
  const content = fs.readFileSync(filePath, 'utf8');
  const words = [];
  const wordRegex = /\{\s*id:\s*'([^']+)',\s*korean:\s*'([^']+)',\s*romanization:\s*'([^']+)',\s*english:\s*'((?:[^'\\]|\\.)*)'\s*,\s*partOfSpeech:\s*'([^']+)',\s*category:\s*'([^']+)'(?:\s*as\s*\w+)?,\s*level:\s*'([^']+)'[\s\S]*?imagePrompt:\s*'([^']+)'/g;
  let match;
  while ((match = wordRegex.exec(content)) !== null) {
    words.push({ id: match[1], korean: match[2], english: match[4], category: match[6], imagePrompt: match[8] });
  }
  return words;
}

const dataDir = path.join(ROOT, 'data');
const allWords = [];
for (const f of ['vocabulary.ts', 'vocab-expansion-1.ts', 'vocab-expansion-2.ts', 'vocab-expansion-3.ts']) {
  const p = path.join(dataDir, f);
  if (fs.existsSync(p)) {
    const parsed = parseWordsFromFile(p);
    console.log(`  Parsed ${parsed.length} from ${f}`);
    allWords.push(...parsed);
  }
}

// Filter to only missing images
const pending = allWords.filter((w) => {
  const dir = path.join(VOCAB_DIR, w.category);
  fs.mkdirSync(dir, { recursive: true });
  return !fs.existsSync(path.join(dir, `${w.id}.png`));
});

console.log(`\nTotal words: ${allWords.length}`);
console.log(`Already generated: ${allWords.length - pending.length}`);
console.log(`Remaining: ${pending.length}`);
console.log(`Concurrency: ${CONCURRENCY}\n`);

let generated = 0;
let failed = 0;
let idx = 0;

async function generateImage(word) {
  const outputPath = path.join(VOCAB_DIR, word.category, `${word.id}.png`);
  const prompt = `Clean, simple flat illustration for a language learning flashcard. ${word.imagePrompt}.
Style: Flat design, minimal, vibrant colors on a clean white background. No text, no words, no letters, no labels. Square 1:1 aspect ratio. Simple and immediately recognizable as "${word.english}".`;

  try {
    const response = await client.models.generateContent({
      model: 'gemini-2.5-flash-image',
      contents: [{ role: 'user', parts: [{ text: prompt }] }],
      config: { responseModalities: ['TEXT', 'IMAGE'] },
    });
    for (const part of response.candidates[0].content.parts) {
      if (part.inlineData) {
        const buffer = Buffer.from(part.inlineData.data, 'base64');
        fs.writeFileSync(outputPath, buffer);
        generated++;
        console.log(`  [${generated + failed}/${pending.length}] âś“ ${word.id} (${word.english}) ${Math.round(buffer.length / 1024)}KB`);
        return;
      }
    }
    failed++;
    console.log(`  [${generated + failed}/${pending.length}] âś— ${word.id} â€” no image returned`);
  } catch (err) {
    failed++;
    console.log(`  [${generated + failed}/${pending.length}] âś— ${word.id} â€” ${err.message}`);
  }
}

async function worker(id) {
  while (true) {
    const i = idx++;
    if (i >= pending.length) break;
    await generateImage(pending[i]);
    // Small stagger to spread API calls
    await new Promise((r) => setTimeout(r, 300));
  }
}

const start = Date.now();
await Promise.all(Array.from({ length: CONCURRENCY }, (_, i) => worker(i)));
const elapsed = ((Date.now() - start) / 1000).toFixed(0);

console.log(`\n=== DONE in ${elapsed}s ===`);
console.log(`Generated: ${generated}`);
console.log(`Failed: ${failed}`);
console.log(`Total images: ${allWords.length - pending.length + generated}`);

// â”€â”€ Regenerate vocabImages.ts â”€â”€
console.log('\nGenerating data/vocabImages.ts mapping...');
const existingImages = allWords.filter((w) => fs.existsSync(path.join(VOCAB_DIR, w.category, `${w.id}.png`)));
let mc = `// Auto-generated by scripts/generate-vocab-images.mjs\n`;
mc += `// Maps vocabulary word IDs to locally bundled images\n\n`;
mc += `const vocabImages: Record<string, any> = {\n`;
for (const w of existingImages) {
  mc += `  '${w.id}': require('@/assets/images/vocab/${w.category}/${w.id}.png'),\n`;
}
mc += `};\n\nexport function getVocabImage(wordId: string): any | null {\n`;
mc += `  return vocabImages[wordId] ?? null;\n}\n\nexport default vocabImages;\n`;
fs.writeFileSync(path.join(ROOT, 'data', 'vocabImages.ts'), mc);
console.log(`Generated mapping for ${existingImages.length} images.`);
