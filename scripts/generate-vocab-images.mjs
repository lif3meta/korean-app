import { GoogleGenAI } from '@google/genai';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');

// Read API key from .env.local
const envFile = fs.readFileSync(path.join(ROOT, '.env.local'), 'utf8');
const API_KEY = envFile.match(/GEMINI_API_KEY=(.+)/)?.[1]?.trim();
if (!API_KEY) {
  console.error('GEMINI_API_KEY not found in .env.local');
  process.exit(1);
}

const client = new GoogleGenAI({ apiKey: API_KEY });

// ── Import vocabulary data by parsing TS files ──
function parseWordsFromFile(filePath) {
  const content = fs.readFileSync(filePath, 'utf8');
  const words = [];
  const wordRegex = /\{\s*id:\s*'([^']+)',\s*korean:\s*'([^']+)',\s*romanization:\s*'([^']+)',\s*english:\s*'((?:[^'\\]|\\.)*)'\s*,\s*partOfSpeech:\s*'([^']+)',\s*category:\s*'([^']+)'(?:\s*as\s*\w+)?,\s*level:\s*'([^']+)'[\s\S]*?imagePrompt:\s*'([^']+)'/g;

  let match;
  while ((match = wordRegex.exec(content)) !== null) {
    words.push({
      id: match[1],
      korean: match[2],
      english: match[4],
      category: match[6],
      imagePrompt: match[8],
    });
  }
  return words;
}

function parseVocabulary() {
  const dataDir = path.join(ROOT, 'data');
  const files = [
    path.join(dataDir, 'vocabulary.ts'),
    path.join(dataDir, 'vocab-expansion-1.ts'),
    path.join(dataDir, 'vocab-expansion-2.ts'),
    path.join(dataDir, 'vocab-expansion-3.ts'),
  ];
  const words = [];
  for (const f of files) {
    if (fs.existsSync(f)) {
      const parsed = parseWordsFromFile(f);
      console.log(`  Parsed ${parsed.length} words from ${path.basename(f)}`);
      words.push(...parsed);
    }
  }
  return words;
}

const vocabulary = parseVocabulary();
console.log(`Found ${vocabulary.length} vocabulary words to generate images for.\n`);

const VOCAB_DIR = path.join(ROOT, 'assets', 'images', 'vocab');

// Get all unique categories
const categories = [...new Set(vocabulary.map((w) => w.category))];

// Create directories
for (const cat of categories) {
  const catDir = path.join(VOCAB_DIR, cat);
  fs.mkdirSync(catDir, { recursive: true });
}

async function generateImage(word, outputPath) {
  const prompt = `Clean, simple flat illustration for a language learning flashcard. ${word.imagePrompt}.
Style: Flat design, minimal, vibrant colors on a clean white background. No text, no words, no letters, no labels. Square 1:1 aspect ratio. Simple and immediately recognizable as "${word.english}".`;

  console.log(`  Generating: ${word.id} (${word.english})...`);

  try {
    const response = await client.models.generateContent({
      model: 'gemini-2.5-flash-image',
      contents: [{ role: 'user', parts: [{ text: prompt }] }],
      config: {
        responseModalities: ['TEXT', 'IMAGE'],
      },
    });

    for (const part of response.candidates[0].content.parts) {
      if (part.inlineData) {
        const buffer = Buffer.from(part.inlineData.data, 'base64');
        fs.writeFileSync(outputPath, buffer);
        console.log(`  Saved: ${word.id} (${Math.round(buffer.length / 1024)}KB)`);
        return true;
      }
    }

    console.error(`  No image generated for ${word.id}`);
    return false;
  } catch (err) {
    console.error(`  Error generating ${word.id}: ${err.message}`);
    return false;
  }
}

// Generate images by category
let generated = 0;
let skipped = 0;
let failed = 0;

for (const cat of categories) {
  const catWords = vocabulary.filter((w) => w.category === cat);
  console.log(`\n=== ${cat.toUpperCase()} (${catWords.length} words) ===`);

  for (const word of catWords) {
    const outputPath = path.join(VOCAB_DIR, word.category, `${word.id}.png`);

    // Skip if already generated
    if (fs.existsSync(outputPath)) {
      console.log(`  Skipping ${word.id} (already exists)`);
      skipped++;
      continue;
    }

    const success = await generateImage(word, outputPath);
    if (success) {
      generated++;
    } else {
      failed++;
    }

    // Small delay to avoid rate limiting
    await new Promise((r) => setTimeout(r, 1000));
  }
}

console.log(`\n=== DONE ===`);
console.log(`Generated: ${generated}`);
console.log(`Skipped: ${skipped}`);
console.log(`Failed: ${failed}`);
console.log(`Total words: ${vocabulary.length}`);

// ── Generate vocabImages.ts mapping file ──
console.log('\nGenerating data/vocabImages.ts mapping...');

const existingImages = [];
for (const word of vocabulary) {
  const imgPath = path.join(VOCAB_DIR, word.category, `${word.id}.png`);
  if (fs.existsSync(imgPath)) {
    existingImages.push(word);
  }
}

let mappingContent = `// Auto-generated by scripts/generate-vocab-images.mjs\n`;
mappingContent += `// Maps vocabulary word IDs to locally bundled images\n\n`;
mappingContent += `const vocabImages: Record<string, any> = {\n`;

for (const word of existingImages) {
  mappingContent += `  '${word.id}': require('@/assets/images/vocab/${word.category}/${word.id}.png'),\n`;
}

mappingContent += `};\n\n`;
mappingContent += `export function getVocabImage(wordId: string): any | null {\n`;
mappingContent += `  return vocabImages[wordId] ?? null;\n`;
mappingContent += `}\n\n`;
mappingContent += `export default vocabImages;\n`;

fs.writeFileSync(path.join(ROOT, 'data', 'vocabImages.ts'), mappingContent);
console.log(`Generated mapping for ${existingImages.length} images.`);
console.log('Done!');
